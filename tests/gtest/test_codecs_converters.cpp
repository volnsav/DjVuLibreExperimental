TEST(CodecConvertersTest, JB2ImageDecodeRejectsShortPrefixes)
{
  GP<JB2Image> image = JB2Image::create();
  image->set_dimension(7, 5);
  const int shape = image->add_shape(MakeOnePixelShape());
  JB2Blit blit;
  blit.left = 2;
  blit.bottom = 1;
  blit.shapeno = shape;
  image->add_blit(blit);

  GP<ByteStream> encoded = EncodeJB2Image(image);
  const std::vector<char> raw = ReadAllBytes(encoded);
  ASSERT_GT(raw.size(), 1u);

  const size_t max_cut = std::min<size_t>(raw.size() - 1, 24u);
  for (size_t cut = 1; cut <= max_cut; ++cut)
  {
    GP<ByteStream> short_bs = ByteStream::create();
    ASSERT_EQ(cut, short_bs->write(&raw[0], cut));
    ASSERT_EQ(0, short_bs->seek(0, SEEK_SET));
    GP<JB2Image> decoded = JB2Image::create();
    EXPECT_THROW(decoded->decode(short_bs), GException);
  }
}

