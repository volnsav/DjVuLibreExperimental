TEST(CodecConvertersTest, JB2DecodeRejectsTruncatedEncodedStream)
{
  GP<JB2Image> image = JB2Image::create();
  image->set_dimension(8, 8);
  const int shape = image->add_shape(MakeOnePixelShape());

  JB2Blit blit;
  blit.left = 3;
  blit.bottom = 4;
  blit.shapeno = shape;
  image->add_blit(blit);

  GP<ByteStream> full = EncodeJB2Image(image);
  ASSERT_EQ(0, full->seek(0, SEEK_END));
  const int len = full->tell();
  ASSERT_GT(len, 4);
  ASSERT_EQ(0, full->seek(0, SEEK_SET));

  char buf[4096] = {0};
  ASSERT_LT(len, static_cast<int>(sizeof(buf)));
  ASSERT_EQ(static_cast<size_t>(len), full->readall(buf, len));

  GP<ByteStream> truncated = ByteStream::create();
  const int cut = len / 2;
  ASSERT_GT(cut, 0);
  ASSERT_EQ(cut, truncated->write(buf, cut));
  ASSERT_EQ(0, truncated->seek(0, SEEK_SET));

  GP<JB2Image> decoded = JB2Image::create();
  EXPECT_THROW(decoded->decode(truncated), GException);
}

TEST(CodecConvertersTest, JB2EncodeDecodeWithShapeHierarchyAndManyBlits)
{
  GP<JB2Image> image = JB2Image::create();
  image->set_dimension(48, 36);

  JB2Shape root = MakeOnePixelShape();
  root.parent = -1;
  const int s0 = image->add_shape(root);

  JB2Shape child = MakeOnePixelShape();
  child.parent = s0;
  const int s1 = image->add_shape(child);

  JB2Shape child2 = MakeOnePixelShape();
  child2.parent = s1;
  const int s2 = image->add_shape(child2);

  for (int y = 0; y < 20; ++y)
  {
    JB2Blit blit;
    blit.left = (y * 3) % 45;
    blit.bottom = (y * 5) % 33;
    blit.shapeno = (y % 3 == 0) ? s0 : ((y % 3 == 1) ? s1 : s2);
    image->add_blit(blit);
  }

  GP<ByteStream> encoded = EncodeJB2Image(image);
  GP<JB2Image> decoded = JB2Image::create();
  decoded->decode(encoded);

  EXPECT_EQ(48, decoded->get_width());
  EXPECT_EQ(36, decoded->get_height());
  EXPECT_EQ(3, decoded->get_shape_count());
  EXPECT_EQ(20, decoded->get_blit_count());
}

