JB2Shape MakeOnePixelShape(void)
{
  JB2Shape shape;
  shape.parent = -1;
  shape.userdata = 0;
  shape.bits = GBitmap::create(1, 1, 0);
  shape.bits->set_grays(2);
  (*shape.bits)[0][0] = 1;
  return shape;
}

GP<ByteStream> EncodeJB2Dict(const GP<JB2Dict> &dict)
{
  GP<ByteStream> bs = ByteStream::create();
  dict->encode(bs);
  bs->seek(0, SEEK_SET);
  return bs;
}

GP<ByteStream> EncodeJB2Image(const GP<JB2Image> &image)
{
  GP<ByteStream> bs = ByteStream::create();
  image->encode(bs);
  bs->seek(0, SEEK_SET);
  return bs;
}

std::vector<char> ReadAllBytes(const GP<ByteStream> &bs)
{
  std::vector<char> out;
  bs->seek(0, SEEK_END);
  const int len = bs->tell();
  out.resize(len > 0 ? len : 0);
  bs->seek(0, SEEK_SET);
  if (len > 0)
  {
    EXPECT_EQ(static_cast<size_t>(len), bs->readall(&out[0], out.size()));
  }
  return out;
}

struct DictCallbackContext
{
  GP<JB2Dict> dict;
};

GP<JB2Dict> ProvideInheritedDict(void *arg)
{
  return static_cast<DictCallbackContext *>(arg)->dict;
}

GP<JB2Dict> ProvideNoDict(void *)
{
  return 0;
}

