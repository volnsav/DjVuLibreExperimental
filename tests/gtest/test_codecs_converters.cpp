TEST(CodecConvertersTest, JB2ImageEncodeDecodeRoundtripKeepsGeometryAndBlits)
{
  GP<JB2Image> image = JB2Image::create();
  image->set_dimension(9, 7);
  image->comment = "codec-image";

  const int s0 = image->add_shape(MakeOnePixelShape());
  const int s1 = image->add_shape(MakeOnePixelShape());

  JB2Blit b0;
  b0.left = 2;
  b0.bottom = 3;
  b0.shapeno = s0;
  image->add_blit(b0);

  JB2Blit b1;
  b1.left = 7;
  b1.bottom = 1;
  b1.shapeno = s1;
  image->add_blit(b1);

  GP<ByteStream> encoded = EncodeJB2Image(image);
  GP<JB2Image> decoded = JB2Image::create();
  decoded->decode(encoded);

  EXPECT_EQ(9, decoded->get_width());
  EXPECT_EQ(7, decoded->get_height());
  EXPECT_EQ(2, decoded->get_shape_count());
  EXPECT_EQ(2, decoded->get_blit_count());

  GP<GBitmap> rendered = decoded->get_bitmap();
  ASSERT_TRUE(rendered != 0);
  EXPECT_GT((*rendered)[3][2], 0);
  EXPECT_GT((*rendered)[1][7], 0);

  const GRect rect(1, 1, 5, 4);
  GP<GBitmap> sub = decoded->get_bitmap(rect, 1, 4, 0);
  ASSERT_TRUE(sub != 0);
  EXPECT_EQ(static_cast<unsigned int>(rect.height()), sub->rows());
  EXPECT_EQ(static_cast<unsigned int>(rect.width()), sub->columns());
}

